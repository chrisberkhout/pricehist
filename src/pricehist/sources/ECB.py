class ECB():

    @staticmethod
    def id():
        return 'ecb'

    @staticmethod
    def name():
        return 'European Central Bank'

    @staticmethod
    def description():
        return 'European Central Bank Euro foreign exchange reference rates'

    @staticmethod
    def source_url():
        return 'https://www.ecb.europa.eu/stats/exchange/eurofxref/html/index.en.html'
